/*1. Contagem Regressiva (Fácil) Enunciado:
Escreva um programa que faça uma contagem 
regressiva de 10 até 0 usando o laço while.*/

function regressiva(numero){
    while(numero >= 0){ //inicia o while ate numero ser menor que zero
           console.log(numero) //imprime valor atual de variavel numero
           numero-- // decrementa o valor de numero -1

    }


}
regressiva(10) //chama function recressiva e envia valor a variavel numero